    // Abstract Data Types (ADTs):
    // Abstract Data Types (ADTs) are a way to define data structures
    // based on the operations that can be performed on them, rather than
    // their implementation details. This allows for greater flexibility
    // and easier maintenance of code.

    // ADTs: Stacks, Queues, Linked Lists, Trees, Graphs, Hash Tables

    // when developing a data structure, it's important to choose the right ADT
    // for the task at hand. This can greatly affect the performance and
    // efficiency of the resulting implementation.

    // always take care of time complexity and space complexity.

    // Time complexity: measure of how the running time of an algorithm grows with the size of the input.
    // Space complexity: measure of how much memory an algorithm uses in relation to the input size.

    // Big O notation: a mathematical notation used to describe the upper bound of the time complexity of an algorithm.
    // It provides a high-level understanding of the algorithm's efficiency and helps in comparing different algorithms.

    // O(1): Constant time complexity - the algorithm takes the same amount of time regardless of input size. eg: linear search in a small array
    // O(log n): Logarithmic time complexity - the algorithm's time grows logarithmically as input size increases. eg: binary search
    // O(n): Linear time complexity - the algorithm's time grows linearly with input size. eg: finding an element in an unsorted array
    // O(n log n): Linearithmic time complexity - the algorithm's time grows in proportion to n log n. eg: merge sort
    // O(n^2): Quadratic time complexity - the algorithm's time grows quadratically with input size. eg: bubble sort

    // concept of linear search:
    // Linear search is a simple algorithm that checks each element in a list sequentially until it finds 
    // the target element or reaches the end of the list. It has a time complexity of O(n) because in 
    // the worst case, it may have to check every element in the list.

    // concept of binary search:
    // Binary search is a more efficient algorithm that works on sorted lists. It repeatedly divides the search interval in half,
    // eliminating the half that cannot contain the target element. It has a time complexity of O(log n) because it reduces the
    // search space by half with each iteration.

    // concept of bubble sort:
    // Bubble sort is a simple sorting algorithm that repeatedly steps through the list, compares 
    // adjacent elements and swaps them if they are in the wrong order. It has a time complexity 
    // of O(n^2) because in the worst case, it may have to compare every element with every other 
    // element.


    




